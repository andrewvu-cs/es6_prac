// LET & CONST VARIABLES

// let - for only variables
// const - const for values that should never change

let myName = 'Andrew';     
console.log(myName);

myName = 'Andres';
console.log(myName);

//Code below will provide an error because myName value cannot be reassigned
const myName = 'Andrew';
console.log(myName);

myName = 'Andres';
console.log(myName);